package com.wfp.db.platform.model;

import java.util.Date;

import com.enterprisehorizons.db.model.BaseModel;
import com.spacetimeinsight.db.model.ICacheableModel;

public class AlertService extends BaseModel implements ICacheableModel {

	private static final long serialVersionUID = 0L;
	
	private Long templateId = null;
	private String receivedMsg = null;
	private Date sendOn = null;
	private Date receivedOn = null;
	private String userId  = null;
	private String eventRefId = null;
	
	public String getEventRefId() {
		return eventRefId;
	}
	public void setEventRefId(String eventRefId) {
		this.eventRefId = eventRefId;
	}
	public Long getTemplateId() {
		return templateId;
	}
	public void setTemplateId(Long templateId) {
		this.templateId = templateId;
	}
	public String getReceivedMsg() {
		return receivedMsg;
	}
	public void setReceivedMsg(String receivedMsg) {
		this.receivedMsg = receivedMsg;
	}
	public Date getSendOn() {
		return sendOn;
	}
	public void setSendOn(Date sendOn) {
		this.sendOn = sendOn;
	}
	public Date getReceivedOn() {
		return receivedOn;
	}
	public void setReceivedOn(Date receivedOn) {
		this.receivedOn = receivedOn;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	
	
	
}
