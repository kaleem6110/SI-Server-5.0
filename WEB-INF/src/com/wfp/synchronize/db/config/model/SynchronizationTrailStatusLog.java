package com.wfp.synchronize.db.config.model;

import java.util.Date;

import com.spacetimeinsight.db.model.BaseAuditModel;

/**
 * @author aditya.velala
 *
 */
public class SynchronizationTrailStatusLog extends BaseAuditModel{

	private static final long serialVersionUID = 2479381035872061921L;
	private String hostname = null;
	private String ackId = null;
	private Long status = null;
	private Date processedDate = null;
	
	
	public String getAckId() {
		return ackId;
	}
	public void setAckId(String ackId) {
		this.ackId = ackId;
	}
	public Long getStatus() {
		return status;
	}
	public void setStatus(Long status) {
		this.status = status;
	}
	public String getHostname() {
		return hostname;
	}
	public void setHostname(String hostname) {
		this.hostname = hostname;
	}
	public Date getProcessedDate() {
		return processedDate;
	}
	public void setProcessedDate(Date processedDate) {
		this.processedDate = processedDate;
	}
	
	
	
	
	
}
