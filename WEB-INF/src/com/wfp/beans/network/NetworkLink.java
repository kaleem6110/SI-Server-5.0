public class NetworkLink {
	
	private String loc;
	private String sw_managed_address;
	private String latitude;
	private String longitude;
	private String sw_business_line;
	private String ipmonitor_id;
	private String region;
	private String country;
	/**
	 * @return the loc
	 */
	public String getLoc() {
		return loc;
	}
	/**
	 * @param loc the loc to set
	 */
	public void setLoc(String loc) {
		this.loc = loc;
	}
	/**
	 * @return the sw_managed_address
	 */
	public String getSw_managed_address() {
		return sw_managed_address;
	}
	/**
	 * @param sw_managed_address the sw_managed_address to set
	 */
	public void setSw_managed_address(String sw_managed_address) {
		this.sw_managed_address = sw_managed_address;
	}
	/**
	 * @return the latitude
	 */
	public String getLatitude() {
		return latitude;
	}
	/**
	 * @param latitude the latitude to set
	 */
	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}
	/**
	 * @return the longitude
	 */
	public String getLongitude() {
		return longitude;
	}
	/**
	 * @param longitude the longitude to set
	 */
	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}
	/**
	 * @return the sw_business_line
	 */
	public String getSw_business_line() {
		return sw_business_line;
	}
	/**
	 * @param sw_business_line the sw_business_line to set
	 */
	public void setSw_business_line(String sw_business_line) {
		this.sw_business_line = sw_business_line;
	}
	/**
	 * @return the ipmonitor_id
	 */
	public String getIpmonitor_id() {
		return ipmonitor_id;
	}
	/**
	 * @param ipmonitor_id the ipmonitor_id to set
	 */
	public void setIpmonitor_id(String ipmonitor_id) {
		this.ipmonitor_id = ipmonitor_id;
	}
	/**
	 * @return the region
	 */
	public String getRegion() {
		return region;
	}
	/**
	 * @param region the region to set
	 */
	public void setRegion(String region) {
		this.region = region;
	}
	/**
	 * @return the country
	 */
	public String getCountry() {
		return country;
	}
	/**
	 * @param country the country to set
	 */
	public void setCountry(String country) {
		this.country = country;
	}

}
